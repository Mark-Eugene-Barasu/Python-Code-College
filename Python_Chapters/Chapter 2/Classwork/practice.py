
# checking out the index[] of a string
word = "Python"
print(word[0])  # Output: P
print(word[5])  # Output: n

# slicing a string
print(word[1:4])  # Output: yth
print(word[:3])   # Output: Pyt
print(word[3:6])  # Output: hon

# indexing
print(word[-1])  # Output: n
print(word[-6])  # Output: P

# using the len() function to find the length of a string
print(f"Length of word: {len(word)}")  # Output: 6

# new line and tab
print("Hello\nWorld")  # Output: Hello (new line) World
print("Hello\tWorld")  # Output: Hello (tab) World
